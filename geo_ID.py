ids = {
    "user1": [213, 213, 213, 15, 213],
    "user2": [54, 54, 119, 119, 119],
    "user3": [213, 98, 98, 35],
}

user = set()  # Множество - заготовка для вывода
for k in (ids.values()):  # Перебор значений словаря, превращение их в множества и суммирование множеств
    user = user.union(set(k))
print(user)
